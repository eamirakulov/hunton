				<p>The goal of an LPS is to prevent or reduce incidents, which are defined as either losses or near
				losses, using behavior-based tools and proven management techniques. The behavior based
				system utilizes a set of tools that identify and eliminate the factors and behaviors that lead to loss,
				including injuries, property damage and operational inefficiencies.</p>
				<p><b>We aim to keep our EMR and TRIR ratings at their current excellent levels. What is an
				Experience Modifier Rating (EMR) or the Osha Total Recordable Incident Rate (TRIR)?</b></p>
				<p>The Experience Modification Rate is a number used by insurance companies to gauge both past
				cost of injuries and future chances of risk. The lower the EMR of your business, the lower your
				worker compensation insurance premiums will be. The TRIR is a commonly used indicator that
				measures a company&#39;s Total Recordable Injury Rate and adjusts for the number of hours a facility
				works. It does not matter how many employees there are, but is a function of the number of
				recordable injuries and the numbers to hours worked (usually including salary and hourly). We are
				consistently better than our industry average which is a TRIR of 4.00 and an EMR that is greater
				than 1.00.</p>
				<p><b>Hunton requires a mandatory Job Safety Analysis for each job. What is a JSA?</b></p>
				<p>A job safety analysis (JSA) is a procedure which helps integrate accepted safety and health
				principles and practices into a particular task or job operation. In a JSA, each basic step of
				the job is to identify potential hazards and to recommend the safest way to do the job.</p>
